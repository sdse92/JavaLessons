package lesson6.calculator;

abstract public class Operation {
    protected int a;
    protected int b;

    public Operation(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

   abstract public int execute();
}
